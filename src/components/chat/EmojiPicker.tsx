export default function EmojiPicker({ onEmojiSelect, children }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false);
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof EMOJI_CATEGORIES>('smileys');
  const [searchQuery, setSearchQuery] = useState('');
  const [recentEmojis, setRecentEmojis] = useState<string[]>(() => {
    const saved = localStorage.getItem('recent-emojis');
    return saved ? JSON.parse(saved) : ['👍', '❤️', '😂', '😮', '😢', '😡'];
  });

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    
    // Add to recent emojis
    const newRecent = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, 6);
    setRecentEmojis(newRecent);
    localStorage.setItem('recent-emojis', JSON.stringify(newRecent));
    
    setIsOpen(false);
  };

  const filteredEmojis = () => {
    if (searchQuery) {
      return Object.values(EMOJI_DATA).flat().filter(emoji => 
        emoji.includes(searchQuery) || 
        emoji.toLowerCase().includes(searchQuery.toLowerCase())
      );
    }
    
    if (selectedCategory === 'recent') {
      return recentEmojis;
    }
    
    return EMOJI_DATA[selectedCategory] || [];
  };

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        {children || (
          <Button variant="ghost" size="sm" className="h-8 w-8 p-0">
            <Smile className="h-4 w-4" />
          </Button>
        )}
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0" align="start">
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              placeholder="Search emojis..."
              value={searchQuery}
              onChange={(e) => setSearchQuery(e.target.value)}
              className="pl-9"
            />
          </div>
        </div>
        
        <div className="flex border-b">
          {Object.entries(EMOJI_CATEGORIES).map(([category, emoji]) => (
            <Button
              key={category}
              variant={selectedCategory === category ? "default" : "ghost"}
              size="sm"
              className="flex-1 rounded-none"
              onClick={() => setSelectedCategory(category as keyof typeof EMOJI_CATEGORIES)}
            >
              <span className="text-lg">{emoji}</span>
            </Button>
          ))}
        </div>
        
        <ScrollArea className="h-64">
          <div className="p-3 grid grid-cols-8 gap-1">
            {filteredEmojis().map((emoji, index) => (
              <Button
                key={`emoji-${selectedCategory}-${index}-${emoji}`}
                variant="ghost"
                size="sm"
                className="h-8 w-8 p-0 text-lg hover:bg-accent"
                onClick={() => handleEmojiClick(emoji)}
              >
                {emoji}
              </Button>
            ))}
          </div>
        </ScrollArea>
      </PopoverContent>
    </Popover>
  );
}
